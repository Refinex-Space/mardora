  '              video: ["video/*"],',
  '              audio: ["audio/*"],',
  '              file: ["*/*"],',
  "            },",
  "          },",
  "          onNodesChange: (nextNodes) => {",
  "            nodes.value = nextNodes;",
  "          },",
  "        }),",
  "        updateListener,",
  "      ],",
  "    }),",
  "  });",
  "}",
