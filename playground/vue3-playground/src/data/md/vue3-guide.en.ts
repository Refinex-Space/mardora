  "## 6. Watch mode and config changes",
  "",
  "```ts",
  "async function rebuildForMode() {",
  "  await nextTick();",
  "  destroyViews();",
  "",
  '  if (mode.value === "view" || mode.value === "output") {',
  "    await renderPreview();",
  "    return;",
  "  }",
  "",
  "  createEditorView();",
  "}",
  "",
  "watch([mode, theme, activePlugins], () => {",
  "  rebuildForMode();",
  "});",
  "",
  "watch(",
  "  config,",
  "  () => {",
  "    rebuildForMode();",
  "  },",
  "  { deep: true }",
  ");",
  "",
  "watch(content, (next) => {",
  "  if (!editorView.value) return;",
  "  if (next === editorView.value.state.doc.toString()) return;",
  "",
  "  internalUpdate = true;",
  "  editorView.value.dispatch({",
  "    changes: {",
  "      from: 0,",
  "      to: editorView.value.state.doc.length,",
  "      insert: next,",
  "    },",
  "  });",
  "  internalUpdate = false;",
  "});",
  "",
  "onMounted(() => {",
  "  rebuildForMode();",
  "});",
  "```",
  "",
  "Note: do not rebuild the `EditorView` on every keystroke. Rebuild only on mode, theme, plugin, or config changes; sync body changes via dispatch.",
  "",
  "## 7. Outline jumping",
  "",
  "```ts",
  "function jumpToc(item: MardoraTocItem) {",
  '  if (mode.value === "view") {',
  "    const target = previewHost.value?.querySelector(`#${CSS.escape(item.id)}`) as HTMLElement | null;",
  '    target?.scrollIntoView({ block: "start" });',
  "    return;",
  "  }",
  "",
  '  if (editorView.value && typeof item.from === "number") {',
  "    editorView.value.dispatch({",
  "      selection: { anchor: item.from },",
  '      effects: EditorView.scrollIntoView(item.from, { y: "start" }),',
  "    });",
  "    editorView.value.focus();",
  "  }",
  "}",
  "```",
  "",
  "The edit-state outline comes from `toc.onTocChange`; the preview-state outline comes from `extractPreviewTocFromMarkdown()`. This way the outline works whether or not an editor instance exists.",
  "",
  "## 8. Lifecycle cleanup",
  "",
  "```ts",
  "function destroyViews() {",
  "  editorView.value?.destroy();",
  "  htmlView.value?.destroy();",
  "  cssView.value?.destroy();",
  "",
  "  editorView.value = null;",
  "  htmlView.value = null;",
  "  cssView.value = null;",
  "",
  "  if (previewStyleElement.value) {",
  "    previewStyleElement.value.remove();",
  "    previewStyleElement.value = null;",
  "  }",
  "}",
  "",
  "onBeforeUnmount(() => {",
  "  destroyViews();",
  "  for (const url of objectUrls) {",
  "    URL.revokeObjectURL(url);",
  "  }",
  "  objectUrls.length = 0;",
  "});",
  "```",
